  <h1 class="text-2xl">Vision</h1>
    
<p>
  It is important we take care of the most vulnerable members of our society. 
  By 2030, 1 in 6 people around the world will be over 60 years old. 
  The over 60s population will have doubled to over 2 billion by 2050, 
  with over 400m people expected to be over 80 years old.
</p>

<p>
As people live longer, we need a solution to ensure their care needs are not just met, 
but we proactively build systems that maximise the health benefits for this age group.
</p>

  <p>
    OpenCare has been designed with carers and family members of care home residents to deliver a more efficient, thoughtful system. 
    OpenCare is designed to make the carers job as easy as possible, enabling them to focus on providing the best care for residents. 
    OpenCare is also designed to give family members peace of mind, giving visibility of the important events they need to know about. 
    Providing this information through a better automated system, will free up carer time, support services time and allow family members to sleep easier.  
  </p>

  <p>
    Carehome operators typically pass costs onto residents, so we aim to take a different approach to ensure a good return on investment for investors to get the system setup.
    We will charge as low a fee as possible to the carehome operators to get them to use the system.
    With maximum uptake of the system we will be able to build a dataset rich with elderly care data.
    We plan to use this data to train models to provide governments with proactive rather than reactive information on how to improve elderly care. 
    Government grants are readily available for AI solutions that solve real world problems and we intend to gather data to ensure patterns can be spotted to help carehome residents.
    Building this dataset will provide the foundation for AI to revolutionise elderly care, decentralising the ownership to ensure the elderly are not exploited. 
  </p>

  <p>Investing in elderly care now is a long term investment for your future.</p>

